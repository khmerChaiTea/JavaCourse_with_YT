
class Hello
{
    public static void main(String args[])
    {
        // If else in Java
        int x = 8;
        int y = 7;

        // if (x > 10 && x <= 20) // 11 - 20
        if (x > y)
        {
            System.out.println(x);
            System.out.println("Thank You");
        }
        else
            System.out.println(y);
    }
}


// Compile in terminal: java + c Hello.java
// Run on console: java Hello